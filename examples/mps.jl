
# Gauge freedom
# In representation of an MPS,
# we could represent each tensor in another basis.
# The resultant MPS represents the same state but the tensors are different.
#
# Furthermore, gauge freedom is the only transformation we could apply
# to the MPS without changing the physical state.


# Infinite MPS
# In the thermodynamic limit, only the largest eigenvalue of the transfer matrix
# is relevant. The rest of the eigenvalues are irrelevant since you are multiplying
# the same eigenvalue to the power of the length of the chain.
#
# The second largest eigenvalue determines how fast the correlation decays.
# \xi = -1 / log(|\lambda_2|)
